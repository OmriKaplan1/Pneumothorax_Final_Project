def get_lung_side(mask):
    """Returns which lung(s) have pneumothorax in a given mask"""
    h, w = mask.shape
    left = mask[:, :w//2].sum() > 0
    right = mask[:, w//2:].sum() > 0
    if left and right:
        return "both"
    elif left:
        return "left"
    elif right:
        return "right"
    else:
        return "none"

